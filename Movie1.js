function clickOnn(showhide){
	
	if(showhide == "show"){
         document.getElementById('action_movies11').style.visibility="visible";
		 document.getElementById('drama_movies11').style.visibility="hidden";
		 document.getElementById('horror_movies11').style.visibility="hidden";
   }else if(showhide == "hide"){
    document.getElementById('action_movies11').style.visibility="hidden"; 
	
   }
}
